def tub_sonlar(son1=1,son2=231234567890123):
  "oraliqdagi tub sonlar chiqaruvchi funksiya"

  tubsonlar=[]
  for n in range(son1,son2+1):
    tub=True

    if n==1:
      tub=False
    elif  n==2:
      tub=True
    else:
      for x in range(2,n):
        if n%x==0:
          tub=False
    if tub:
      tubsonlar.append(n)
  return tubsonlar

print(tub_sonlar(8,5675675787))

